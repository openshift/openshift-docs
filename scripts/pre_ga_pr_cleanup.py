import subprocess
import json
import sys
import time
from typing import List, Dict, Optional


# Constants
REPO = "openshift/openshift-docs"
BASE_BRANCH = "main"
MILESTONE = "Continuous release"
MAX_SEARCH_LIMIT = 1000
API_SLEEP_DELAY = 1 # Seconds to wait between API calls


def run_gh_command(args: List[str]) -> tuple[bool, str]:
    """
    Execute a gh CLI command and return success status and output.

    Args:
        args: List of command arguments to pass to gh

    Returns:
        Tuple of (success: bool, output: str)
    """
    try:
        result = subprocess.run(
            ["gh"] + args,
            capture_output=True,
            text=True,
            check=False
        )
        return (result.returncode == 0, result.stdout if result.returncode == 0 else result.stderr)
    except FileNotFoundError:
        print("Error: 'gh' CLI not found. Please install GitHub CLI: https://cli.github.com/")
        sys.exit(1)


